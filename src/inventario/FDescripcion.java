    public boolean validarNumero(String cadena){
        try {
            Double.parseDouble(cadena);
            return true;
            } catch (NumberFormatException nfe){
            return false;
        }
    }
    public boolean validarTodo(){
        if(!"".equals(this.jTextField1.getText()) &&
        !"".equals(this.jTextField2.getText()) &&
        !"".equals(this.jTextField3.getText()) &&
        !"".equals(this.jTextField4.getText()) &&
        !"".equals(this.jTextField5.getText()) &&
        !"".equals(this.jTextField6.getText())) {
            if(this.validarNumero(jTextField3.getText()) &&
                    this.validarNumero(jTextField4.getText()) &&
                    this.validarNumero(jTextField5.getText())){
                        return true;
            }else{
                JOptionPane.showMessageDialog(null, "Debe ingresar números en las casillas: cantidad y en los precios. Recuerde no utilizar ni puntos ni comas");
                return false;
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe llenar todas las casillas");
            return false;
        }
    }
    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        if(!"".equals(this.jTextField1.getText())){
            this.jButton5.setEnabled(true);
            resultado=objManejadora.consultaRef(this.jTextField1.getText());
            try{
            this.jTextField2.setText(resultado.getString(2));
            this.jTextField3.setText(""+resultado.getInt(3));
            this.jTextField4.setText(""+resultado.getInt(4));
            this.jTextField5.setText(""+resultado.getInt(5));
            this.jTextField6.setText(resultado.getString(6));
            refTemp=this.jTextField1.getText();
            }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"No se pudo encontrar el producto buscado."+e);
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Digite una referencia válida");
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

